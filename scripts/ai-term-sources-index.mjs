#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import matter from "gray-matter";

const workspaceRoot = process.cwd();
const sourcesDir = path.join(workspaceRoot, "summery", "aiterms", "sources");
const indexPath = path.join(sourcesDir, "index.json");

function inferSlug(value) {
  return (
    String(value ?? "")
      .toLowerCase()
      .trim()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "") || "ai-term"
  );
}

function text(value) {
  return typeof value === "string" ? value.trim() : "";
}

function stringList(value) {
  if (!Array.isArray(value)) {
    return [];
  }
  return [...new Set(value.map((item) => String(item).trim()).filter(Boolean))];
}

function countSourceLinks(markdown) {
  const referenceSection = markdown.match(/##\s+参考资料([\s\S]*)/);
  const target = referenceSection?.[1] ?? markdown;
  return (target.match(/\]\(https?:\/\//g) ?? []).length;
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  await fs.mkdir(sourcesDir, { recursive: true });
  const entries = await fs.readdir(sourcesDir, { withFileTypes: true });
  const items = [];

  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith(".md") || entry.name.toLowerCase() === "readme.md") {
      continue;
    }

    const filePath = path.join(sourcesDir, entry.name);
    const markdown = await fs.readFile(filePath, "utf8");
    const parsed = matter(markdown);
    const firstTitle = parsed.content.match(/^#\s+(.+)$/m)?.[1]?.trim();
    const term = text(parsed.data.term) || firstTitle || path.basename(entry.name, ".md");
    const slug = text(parsed.data.slug) || inferSlug(term);
    const aliases = stringList(parsed.data.aliases);
    const lastVerifiedAt = text(parsed.data.last_verified_at);
    const sourceCount = Number.isInteger(parsed.data.source_count) ? parsed.data.source_count : countSourceLinks(markdown);

    items.push({
      term,
      slug,
      aliases,
      source_file: path.posix.join("summery", "aiterms", "sources", entry.name),
      last_verified_at: lastVerifiedAt,
      source_count: sourceCount,
    });
  }

  items.sort((a, b) => a.slug.localeCompare(b.slug));
  const payload = {
    schema_version: 1,
    updated_at: new Date().toISOString().slice(0, 10),
    note: "Generated by npm run ai-term:sources:index. Do not feed the whole sources directory to GPT; read this index first, then only the matched source_file.",
    items,
  };

  const previous = (await pathExists(indexPath)) ? await fs.readFile(indexPath, "utf8") : "";
  const next = `${JSON.stringify(payload, null, 2)}\n`;
  if (previous !== next) {
    await fs.writeFile(indexPath, next, "utf8");
  }

  console.log(`AI term sources index: ${path.relative(workspaceRoot, indexPath)}`);
  console.log(`Indexed source cards: ${items.length}`);
}

main().catch((error) => {
  console.error(`ERROR: ${error instanceof Error ? error.message : String(error)}`);
  process.exit(1);
});
